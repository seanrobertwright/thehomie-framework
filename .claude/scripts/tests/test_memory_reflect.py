def _legacy_load_current_memory(memory_file: Path) -> str:
    if memory_file.exists():
        return memory_file.read_text(encoding="utf-8")
    return ""


def _legacy_load_user_file(user_file: Path) -> str:
    if user_file.exists():
        return user_file.read_text(encoding="utf-8")
    return ""


def _legacy_load_soul_file(soul_file: Path) -> str:
    if soul_file.exists():
        return soul_file.read_text(encoding="utf-8")
    return ""


def _legacy_load_goals_file(goals_file: Path) -> str:
    if goals_file.exists():
        return goals_file.read_text(encoding="utf-8")
    return ""


def _legacy_load_self_file(self_file: Path) -> str:
    if self_file.exists():
        return self_file.read_text(encoding="utf-8")
    return ""


def _legacy_build_reflect_identity_section(memory_dir: Path) -> str:
    """Pre-refactor identity-section assembly for memory_reflect.py.

    Mirrors the prompt body at memory_reflect.py:202-236 verbatim — same
    ordering (MEMORY/USER/SOUL/SELF/GOALS) and same headers. This helper
    is the parity baseline; behaviour change here = behaviour change in
    production.
    """
    current_memory = _legacy_load_current_memory(memory_dir / "MEMORY.md")
    current_user = _legacy_load_user_file(memory_dir / "USER.md")
    current_soul = _legacy_load_soul_file(memory_dir / "SOUL.md")
    current_goals = _legacy_load_goals_file(memory_dir / "GOALS.md")
    current_self = _legacy_load_self_file(memory_dir / "SELF.md")

    return f"""## Current MEMORY.md

{current_memory}

## Current USER.md

{current_user}

## Current SOUL.md

{current_soul}

## Current SELF.md

{current_self}

## Current GOALS.md (read-only context — do NOT edit this file during reflection)

{current_goals}"""


# F2 post-build fix: production helper IS the test target. Drift in the
