from memory_reflect import _assemble_reflect_identity_section


# =============================================================================
# PARITY TESTS
# =============================================================================


class TestPromptParityWithShim:
    def test_prompt_parity_with_shim(self, reflect_memory_dir: Path) -> None:
        """Legacy + shim-based identity section are byte-identical.

        WS3 acceptance criterion ``memory_reflect_refactor_parity_preserved``
        (verification: ``pytest tests/test_memory_reflect.py::test_prompt_parity_with_shim``).
        """
        legacy = _legacy_build_reflect_identity_section(reflect_memory_dir)
        new = _assemble_reflect_identity_section(reflect_memory_dir)

        assert legacy == new, (
            "Identity-section parity broken between legacy reads + shim. "
            "Refactor introduced a behavior change. Diff first 200 chars:\n"
            f"  legacy[:200]={legacy[:200]!r}\n"
            f"  new[:200]={new[:200]!r}"
        )

    def test_prompt_parity_with_shim_missing_files(self, tmp_path: Path) -> None:
        """Missing identity files → both paths return empty strings, no exception.

        Validates the fail-open contract: ``read_file_safe`` returns ``""`` and
        the shim's ``payload.get(name, '')`` idiom resolves the same way.
        """
        empty_dir = tmp_path / "TheHomie" / "Memory"
        empty_dir.mkdir(parents=True)

        legacy = _legacy_build_reflect_identity_section(empty_dir)
        new = _assemble_reflect_identity_section(empty_dir)

        assert legacy == new
        # Confirm both paths actually emitted empty content blocks (no crash,
        # no path leakage in the rendered output).
        assert "## Current MEMORY.md\n\n\n\n## Current USER.md" in new

    def test_prompt_parity_with_shim_partial_files(self, tmp_path: Path) -> None:
        """Mixed presence (some files exist, some missing) preserves parity."""
        memory_dir = tmp_path / "TheHomie" / "Memory"
        memory_dir.mkdir(parents=True)

        # Only seed MEMORY.md and SELF.md; USER/SOUL/GOALS are absent.
        (memory_dir / "MEMORY.md").write_text(
            "# MEMORY\nentry one\n", encoding="utf-8"
        )
        (memory_dir / "SELF.md").write_text(
            "# SELF\npattern A\n", encoding="utf-8"
        )

        legacy = _legacy_build_reflect_identity_section(memory_dir)
        new = _assemble_reflect_identity_section(memory_dir)

        assert legacy == new
        assert "entry one" in new
        assert "pattern A" in new
